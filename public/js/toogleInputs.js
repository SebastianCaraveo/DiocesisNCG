document.getElementById('flexCheckDefault').addEventListener('change', function() {
    var capillaInput = document.getElementById('capillaInput');
    if (this.checked) {
        capillaInput.style.display = 'block';
    } else {
        capillaInput.style.display = 'none';
    }
});

